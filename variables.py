# ----------------- Rules for Variables Name -------------------------

"""
    1. Variable name should not start with integer Value.
    2. Variable name should not contain any special character or Arithmetic
    3. Variable name should not separated by space 
    Examples:
"""
# Invalid Variables Name:
# 1a = 20
# a@b = 20
# a b = 20

# Valid  Variables Name:

# a_b = 20
# ab = 20
# _ab = 20
# a_b_2 = 20
# AB = 20

